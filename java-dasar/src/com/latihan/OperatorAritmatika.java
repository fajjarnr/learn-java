package com.latihan;

public class OperatorAritmatika {
    public static void main(String[] args){
        int a = 10;
        int b = 20;
        int result;

        result = a + b;
        result = a - b;
        result = a * b;
        result = a / b;
        result = a % b;
        System.out.println(result);
    }
}
