package com.latihan;

public class OperatorAssignment {
    public static void main(String[] args){
        int a = 10;
        a += 10;
        System.out.println(a);

        a -= 10;
        System.out.println(a);

        a *= 10;
        System.out.println(a);

        a /= 10;
        System.out.println(a);
    }
}
